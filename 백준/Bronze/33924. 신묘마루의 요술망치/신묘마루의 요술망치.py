n,m=map(int,input().split())
k=int(input())
s=list(input())

l=[[0,0],[0,0],[0,0],[0,0]]
l[n-1][m-1]=1

for i in s:
    if i == 'A':
        l[0:2],l[2:4]=l[2:4],l[0:2]
    elif i == 'B':
        l[0][0],l[1][1]=l[1][1],l[0][0]
        l[0][1],l[1][0]=l[1][0],l[0][1]
        l[2][0],l[3][1]=l[3][1],l[2][0]
        l[2][1],l[3][0]=l[3][0],l[2][1]
    elif i == 'C':
        l[0][0],l[3][1]=l[3][1],l[0][0]
        l[0][1],l[3][0]=l[3][0],l[0][1]
        l[1][1],l[2][0]=l[2][0],l[1][1]
        l[1][0],l[2][1]=l[2][1],l[1][0]
    elif i == 'D':
        ll=[[0,0],[0,0],[0,0],[0,0]]
        ll[0][0]=l[1][0]
        ll[0][1]=l[0][0]
        ll[1][1]=l[0][1]
        ll[2][1]=l[1][1]
        ll[3][1]=l[2][1]
        ll[3][0]=l[3][1]
        ll[2][0]=l[3][0]
        ll[1][0]=l[2][0]
        l=ll
for i in range(4):
    for j in range(2):
        if l[i][j]==1:
            print(i+1,j+1)
            break